int		ft_sqrt(int nb)
{
	int i;
	int res;

	i = 1;
	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	while (i <= (nb / 2))
	{
		res = i * i;
		if (nb == res)
			return (i);
		i++;
	}
	if (res == nb)
		return (i);
	return (0);
}
